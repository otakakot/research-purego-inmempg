-- =============================================================================
-- TODO [P3]: Network containment operators
-- =============================================================================

-- << (is contained by)
SELECT inet '192.168.1.5' << inet '192.168.1.0/24' AS contained;

-- <<= (is contained by or equals)
SELECT inet '192.168.1.0/24' <<= inet '192.168.1.0/24' AS contained_or_eq;

-- >> (contains)
SELECT inet '192.168.1.0/24' >> inet '192.168.1.5' AS contains;

-- >>= (contains or equals)
SELECT cidr '192.168.0.0/16' >>= cidr '192.168.1.0/24' AS contains_or_eq;

-- && (contains or is contained by)
SELECT inet '192.168.1.0/24' && inet '192.168.0.0/16' AS overlap;

-- =============================================================================
-- TODO [P3]: abbrev() — abbreviated display
-- =============================================================================

SELECT abbrev(inet '192.168.1.5/24') AS abbrev_inet;
SELECT abbrev(cidr '192.168.1.0/24') AS abbrev_cidr;

-- =============================================================================
-- TODO [P3]: broadcast() — broadcast address for network
-- =============================================================================

SELECT broadcast(inet '192.168.1.5/24') AS bcast;

-- =============================================================================
-- TODO [P3]: family() — address family (4 or 6)
-- =============================================================================

SELECT family(inet '192.168.1.1') AS ipv4_family;
SELECT family(inet '::1')         AS ipv6_family;

-- =============================================================================
-- TODO [P3]: host() — IP address as text (without mask)
-- =============================================================================

SELECT host(inet '192.168.1.5/24') AS host_part;

-- =============================================================================
-- TODO [P3]: hostmask() — host mask for network
-- =============================================================================

SELECT hostmask(inet '192.168.1.5/24') AS hmask;

-- =============================================================================
-- TODO [P3]: masklen() — netmask length
-- =============================================================================

SELECT masklen(inet '192.168.1.5/24') AS mlen;

-- =============================================================================
-- TODO [P3]: netmask() — network mask
-- =============================================================================

SELECT netmask(inet '192.168.1.5/24') AS nmask;

-- =============================================================================
-- TODO [P3]: network() — network part of address
-- =============================================================================

SELECT network(inet '192.168.1.5/24') AS net_part;

-- =============================================================================
-- TODO [P3]: set_masklen() — set netmask length
-- =============================================================================

SELECT set_masklen(inet '192.168.1.5/24', 16) AS new_mask_inet;
SELECT set_masklen(cidr '192.168.1.0/24', 16) AS new_mask_cidr;

-- =============================================================================
-- TODO [P3]: text() — convert to text
-- =============================================================================

SELECT text(inet '192.168.1.5/24') AS inet_text;

-- =============================================================================
-- TODO [P3]: inet_merge() — smallest network containing both addresses
-- =============================================================================

SELECT inet_merge(inet '192.168.1.0/24', inet '192.168.2.0/24') AS merged;

-- =============================================================================
-- TODO [P3]: inet_same_family() — check if same address family
-- =============================================================================

